    DichVu getModel() {
        DichVu dv = new DichVu();

        dv.setMaDV(txtMaDv.getText());
        dv.setTenDV(txtTenDv.getText());
        dv.setGia(Float.parseFloat(txtGia.getText()));
        
        Date date = XDate.toDate(txtNgayTao.getText(), "yyyy-MM-dd");
        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        dv.setNgayTao(sqlDate);
        
        dv.setNguoiTao(txtNguoiTao.getText());
        dv.setTrangThai(cboTrangThai.getSelectedItem().toString());
        dv.setHinh(personalImg);

        return dv;
    }
    
    void setModel(DichVu model) {
        txtMaDv.setText(model.getMaDV());
        txtTenDv.setText(model.getTenDV());
        txtGia.setText("" + model.getGia());
        txtNguoiTao.setText("" + model.getNguoiTao());
        try {
            if (model.getHinh() != null) {
                Image img = XImage.createImgFromByteArray(model.getHinh(), "jpg");
                lblImg.setIcon(new ImageIcon(img));
                personalImg = model.getHinh();
            } else {
                ImageIcon icon = new javax.swing.ImageIcon("D:\\DA_JAVA_1\\DA_1\\src\\main\\java\\Icons\\image.png");
                lblImg.setIcon(icon);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        txtNgayTao.setText(""+model.getNgayTao());
        if (model.getTrangThai().equalsIgnoreCase("Còn")) {
            cboTrangThai.setSelectedIndex(0);
        } else {
            cboTrangThai.setSelectedIndex(1);

        }
    }
    
    
    void insert() {
        DichVu dv = getModel();

        try {
            dao.insert(dv);
//            this.fillTable();
//            this.clear();
            MessDiaLogHelper.showMessDiaLog(this, "Thêm mới thành công", "Thông báo");
        } catch (Exception e) {
            e.printStackTrace();
            MessDiaLogHelper.showMessDiaLog(this, "Thêm mới thất bại", "Thông báo");
        }
    }

    void update() {
        DichVu dv = getModel();
        try {
            dao.update(dv);
            MessDiaLogHelper.showMessDiaLog(this, "Cập nhật thành công", "Thông báo");
        } catch (Exception e) {
            MessDiaLogHelper.showMessDiaLog(this, "Cập nhật thất bại", "Thông báo");

        }
    }
    
    void fillToForm(){
        
        try {
            DichVu dv = dao.selectById(maDv);
            setModel(dv);
        } catch (Exception ex) {
            ex.printStackTrace();
            MessDiaLogHelper.showErrorDiaLog(this, "Lỗi truy vấn", "Lỗi");
        }
    }
