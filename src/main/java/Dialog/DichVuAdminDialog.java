    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        if (!Validator.isNumber(this, txtGia.getText())) {
            txtGia.setBackground(Color.yellow);
            return;
        }
        try {
            if (dao.selectById(txtMaDv.getText()) != null) {
                if (MessDiaLogHelper.showConfirmDiaLog(this, "Bạn có muốn cập nhật?", "Xác nhận") == 1) {
                    return;
                }
                update();
            } else {
                insert();
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessDiaLogHelper.showErrorDiaLog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnOkActionPerformed

    private byte[] personalImg;
    private void lblImgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImgMouseClicked
        JFileChooser chooser = new JFileChooser();

        chooser.setFileFilter(new FileFilter() {
            @Override
            public boolean accept(File f) {
                if (f.isDirectory()) {
                    return true;
                } else {
                    return f.getName().toLowerCase().endsWith(".jpg");
                }
            }

            @Override
            public String getDescription() {
                return "Image File (*.jpg)";
            }
        });
        if (chooser.showOpenDialog(this) == 1) {
            return;
        }
        File file = chooser.getSelectedFile();
        try {
            ImageIcon icon = new ImageIcon(file.getPath());
            Image img = XImage.resize(icon.getImage(), 174, 210);
            ImageIcon resizedIcon = new ImageIcon(img);
            lblImg.setIcon(resizedIcon);
            personalImg = XImage.toByteArray(img, "jpg");
        } catch (Exception e) {
            e.printStackTrace();
            MessDiaLogHelper.showErrorDiaLog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_lblImgMouseClicked

